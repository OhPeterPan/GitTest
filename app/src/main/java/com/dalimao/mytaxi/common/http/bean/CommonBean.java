package com.dalimao.mytaxi.common.http.bean;

public class CommonBean {
    private int code;
    private String msg;

    public CommonBean() {
    }

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
